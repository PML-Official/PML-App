class Color {
    constructor(name, r, g, b) {
        this.name = name;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}

const black = new Color("black", 0, 0, 0);
const white = new Color("white", 255, 255, 255);
const richblue = new Color("rich-blue", 0, 0, 255);
const richgreen = new Color("rich-green", 0, 255, 0);
const richred = new Color("rich-red", 255, 0, 0);
